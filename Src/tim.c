void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)	//timing time of Timer2:  20us 50kHz
{
	static uint32_t tim_LED_cnt;
	if(htim == &htim2)					
	{
		/***********20us的执行周期******/
		
		ADS8674_GetCurrent(	);																					//获取采样值

		Current_Loop_Control(Board_State);															//只有running状态电流环才起作用	
		
		Board_ADC_Err_DEC_Hard();																				//检测ADC硬件转换错误
		Board_Coil_OverCurrent_DEC();																		//过流保护-比较重要放到硬件检测等任务的后面防止错误信息被覆盖
		
		/**** End 20us执行周期的任务*****/

		/***********200ms的执行周期******/
		if(++tim_LED_cnt>Time_200ms)
		{
			tim_LED_cnt=0;
			BoardLED_State_Update(Board_State,Board_Running_Info);
//			Board_Coil_OpenOpenCircuit_DEC();														//开路检测 这里为了调试方便，去掉了开路检测
			Board_CANBus_OverTime_DEC();																	//Can总线通讯超时检测
		}
		/**** End 200ms执行周期的任务*****/		
	}
}

